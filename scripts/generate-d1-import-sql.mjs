#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const historyPath = path.join(repoRoot, "data", "playercount-history.json");
const historyTxtPath = path.join(repoRoot, "data.txt");
const statsPath = path.join(repoRoot, "data", "playercount-stats.json");
const outPath = path.join(repoRoot, "worker", "migrations", "0002_import_legacy_data.sql");

const args = process.argv.slice(2);
function getArg(name) {
  const idx = args.indexOf(name);
  if (idx === -1 || idx + 1 >= args.length) return null;
  return args[idx + 1];
}

function readJson(filePath, fallback) {
  try {
    const raw = fs.readFileSync(filePath, "utf8");
    return JSON.parse(raw);
  } catch {
    return fallback;
  }
}

const historyRaw = fs.existsSync(historyTxtPath)
  ? readJson(historyTxtPath, [])
  : readJson(historyPath, []);
const statsRaw = readJson(statsPath, {});

const history = Array.isArray(historyRaw)
  ? historyRaw
      .map((p) => ({
        t: Number(p && p.t),
        v: Number(p && p.v),
      }))
      .filter((p) => Number.isFinite(p.t) && Number.isFinite(p.v))
      .sort((a, b) => a.t - b.t)
  : [];

const cliAllTimeHigh = Number(getArg("--all-time-high"));
const cliAllTimeHighAt = Number(getArg("--all-time-high-at"));
const allTimeHigh = Number.isFinite(cliAllTimeHigh) ? cliAllTimeHigh : Number(statsRaw && statsRaw.allTimeHigh);
const allTimeHighAt = Number.isFinite(cliAllTimeHighAt)
  ? cliAllTimeHighAt
  : Number(statsRaw && statsRaw.allTimeHighAt);
const safeHigh = Number.isFinite(allTimeHigh) ? Math.max(0, Math.trunc(allTimeHigh)) : 0;
const safeHighAt = Number.isFinite(allTimeHighAt) ? Math.max(0, Math.trunc(allTimeHighAt)) : 0;

const lines = [];
lines.push("-- Auto-generated by scripts/generate-d1-import-sql.mjs");
lines.push("-- Imports legacy JSON history into D1 tables.");
lines.push(`-- Source history: ${fs.existsSync(historyTxtPath) ? "data.txt" : "data/playercount-history.json"}`);
lines.push("DELETE FROM playercount_history;");
lines.push(
  "INSERT OR IGNORE INTO playercount_stats (id, all_time_high, all_time_high_at) VALUES (1, 0, 0);"
);

if (history.length) {
  lines.push("INSERT INTO playercount_history (t, v) VALUES");
  lines.push(
    history
      .map((p) => `  (${Math.trunc(p.t)}, ${Math.trunc(p.v)})`)
      .join(",\n") + ";"
  );
}

lines.push(
  `UPDATE playercount_stats SET all_time_high = ${safeHigh}, all_time_high_at = ${safeHighAt} WHERE id = 1;`
);
lines.push("");

fs.writeFileSync(outPath, lines.join("\n"), "utf8");
console.log(
  `Wrote ${outPath} with ${history.length} history points and all-time high ${safeHigh} at ${safeHighAt}.`
);
